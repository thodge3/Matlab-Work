function [ y ] = piecewiseH1( x )

x1 = x(x <= 4);
y(find(x <= 4)) = 112.5*x1;


y(find(4 <= x & x <= 8)) = 450;

x2 = x(8 <= x & x <= 13);
y(find(8 <= x & x <= 13)) = 80* x2 - 190;

y(find(13 <= x & x <= 20)) = 850;

x3 = x(20 <= x & x <= 21); 
y(find(20 <= x & x <= 21)) = -100 * x3 + 2850;


x4 = x(21 <= x & x <= 21.016); 
y(find(21 <= x & x <= 21.016)) = -36000*x4 + 756750;

y(find(21.016 <= x & x <= 29.016)) = 150;


end

